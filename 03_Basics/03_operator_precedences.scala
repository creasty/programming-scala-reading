// メソッドの語尾がコロン `:` の場合、左側**ではなく**右側に束縛される。
val list = List('b', 'c', 'd')

println('a' :: list)   // `::` はいわゆる cons 。リストの先頭に要素を追加できる
println(list.::('a'))  // これと同じ意味


// 左結合、右結合が混じった場合
println('a' :: list ++ List('e', 'f'))
println('a' :: (list ++ List('e', 'f')))  // こっちの解釈になる
